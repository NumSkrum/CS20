
public class Evens {

	public static void main(String[] args) 
	{
		
	
		
		int number = 20;
		System.out.println("Even Numbers from 1 to "+number+" are: ");
		for (int i = 1; i <= number; i++) {
		   //if number%2 == 0 it means its an even number
		   if (i % 2 == 0) {
			System.out.println(i + " ");
		   }
		}
		

		
	}

}
