package GUi;

public class MySavingObj {
	
	public double T = 0;

	public double Total() {
		
		T = T;
				
		return T;
		
	}
		
	public void Penny() 
	{
		
		T = T + 0.01;
		
	
	}
	public void Nickel()
	{
		T = T + 0.05;
		
	}
	public void Dime()
	{
		T = T + 0.10;
		
	}
	public void Quarter()
	{
		T = T + 0.25;
		
	}

}
