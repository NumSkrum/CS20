package GUi;

public class MySavingObj {

	public static double Quarter(double Quarter)
	{
		Quarter = Quarter + 0.25;
		
		return Quarter;
	}
	
	public static double Dimes(double Dimes) 
	{
		Dimes = Dimes + 0.10;
		
		return Dimes;
	}
	
	public static double Nickels(double Nickels) 
	{
		Nickels = Nickels + 0.05;
		
		return Nickels;
	}
	
	public static double Penny(double Penny)
	{
		Penny = Penny + 0.01;
		
		return Penny;
	}
	
}
